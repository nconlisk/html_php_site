<p> Doppler ultrasound provides a measurement of blood velocity, from which
related quantities including volumetric flow and wall shear stress can be
derived. Changes in these quantities can provide powerful non-invasive data
directly related to the extent and effect of arterial disease and of the
effect of other diseases on flow. However, it is known that clinical Doppler
ultrasound velocity measurement errors may be large (>100%)%) if
inappropriate or manufacturer-recommended protocols are used, but that
considerable reduction in error may be achieved using modified protocols. As
preclinical, high-frequency ultrasound systems are increasingly used for
measurement of blood velocity and its related quantities, it is important to
investigate these errors.</p>

<p>We have created a number of test objects (phantoms), and have characterised
a material for use in high-frequency ultrasound phantoms. These phantoms
provide known values of velocity with which the measured values from
preclinical ultrasound can be compared. We also aim to compare the
non-invasive ultrasound measurements to invasive flow probes, the current
gold standard technique for flow measurements.</p>

<p>With this knowledge, errors in preclinical ultrasound Doppler measurements
can be corrected. The outcome of this work will be well-validated test tools
and measurement protocols that will improve the reliability of measurements
made in preclinical investigations. 
